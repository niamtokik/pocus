-module(pocus_prng_sup).
-behavior(supervisor).
-export([start_link/1, init/1]).

%%--------------------------------------------------------------------
%%
%%--------------------------------------------------------------------
start_link(_Args) ->
    supervisor:start_link({local, ?MODULE}, ?MODULE, []).

%%--------------------------------------------------------------------
%%
%%--------------------------------------------------------------------
init(_Args) ->
    {ok, {supervisor_flags(), children()}}.

%%--------------------------------------------------------------------
%%
%%--------------------------------------------------------------------
supervisor_flags() ->
    # { strategy => one_for_one
      , intensity => 0
      , period => 1
      }.

%%--------------------------------------------------------------------
%%
%%--------------------------------------------------------------------
children() ->
    [].

