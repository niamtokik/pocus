%%%===================================================================
%%% @doc pocus top level supervisor.
%%% @end
%%%===================================================================
-module(pocus_sup).
-behaviour(supervisor).
-export([start_link/0]).
-export([init/1]).

%%--------------------------------------------------------------------
%%
%%--------------------------------------------------------------------
start_link() ->
    supervisor:start_link({local, ?MODULE}, ?MODULE, []).

%%--------------------------------------------------------------------
%%
%%--------------------------------------------------------------------
init([]) ->
    {ok, {supervisor_flags(), children()}}.


%%--------------------------------------------------------------------
%%
%%--------------------------------------------------------------------
supervisor_flags() ->
    # { strategy => one_for_one
      , intensity => 0
      , period => 1
      }.

%%--------------------------------------------------------------------
%%
%%--------------------------------------------------------------------
children() ->
    [].

%%--------------------------------------------------------------------
%%
%%--------------------------------------------------------------------
pocus_prng() ->
    #{ id => pocus_prng
     , start => {pocus_prng, start_link, []}
     , type => worker
     }.

%%--------------------------------------------------------------------
%
%%--------------------------------------------------------------------
pocus_prng_sup() ->
    #{ id => pocus_prng_sup
     , start => {pocus_prng_sup, start_link, []}
     , type => supervisor
     }.


